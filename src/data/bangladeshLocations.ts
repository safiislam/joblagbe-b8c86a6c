// Get all locations as flat array "district - upazila"
export const getAllLocations = (): string[] => {
  const locations: string[] = [];
  for (const [district, upazilas] of Object.entries(bangladeshLocations)) {
    locations.push(district);
    for (const upazila of upazilas) {
      locations.push(`${district}, ${upazila}`);
    }
  }
  return locations;
};

